# Operador lógico "E" (AND)
valor1 = true
valor2 = false

resultado_e = valor1 && valor2
puts "Operador 'E' (AND): #{valor1} && #{valor2} = #{resultado_e}"

# Operador lógico "OU" (OR)
valor1 = true
valor2 = false

resultado_ou = valor1 || valor2
puts "Operador 'OU' (OR): #{valor1} || #{valor2} = #{resultado_ou}"

# Operador lógico "NÃO" (NOT)
valor = true

resultado_nao = !valor
puts "Operador 'NÃO' (NOT): !#{valor} = #{resultado_nao}"

